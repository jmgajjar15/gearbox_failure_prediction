import streamlit as st
import pandas as pd
import joblib

model = joblib.load("gearbox_failure_model.pkl")
st.title("Gearbox Failure Prediction System")
st.write("Check whether your machine will work or fail")

Load_percent = st.number_input("Load %")
Temperature_C = st.number_input("Temperature C")
Vibration_mm_s = st.number_input("Vibration mm/s")
Oil_Level_percent = st.number_input("Oil Level %")


df = pd.DataFrame({
    "Load_%": [ Load_percent ],
    "Temperature_C": [Temperature_C],
    "Vibration_mm_s": [Vibration_mm_s],
    "Oil_Level_%": [ Oil_Level_percent ]
})

if st.button("Predict"):
    prediction = model.predict(df)
    if prediction[0] == 1:
        st.error("Gear will fail")
    else:
        st.success("Gear will work")